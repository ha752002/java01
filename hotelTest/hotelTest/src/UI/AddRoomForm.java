    private void yesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_yesActionPerformed

        Room room = new Room();
        room_num = room_number.getText();
        type_room = cbtype.getSelectedItem().toString();
        floor = cbfloor.getSelectedItem().toString();
        status = cbstatus.getSelectedItem().toString();
        room.setPrice(Float.parseFloat(price.getText()));
        room.setImage(filename);
        if (room_num.equals("") || price.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Vui lòng nhập đầy đủ");
        } else {
            Room r1 = new Room(room_num, type_room, floor, status, Float.parseFloat(price.getText()), filename);
            RoomManagement.add(r1);
            dispose();     
        }


    }//GEN-LAST:event_yesActionPerformed

