    private void filter(String query) {
        TableRowSorter<DefaultTableModel> tb = new TableRowSorter<DefaultTableModel>(defaultTableModel);
        roomTable.setRowSorter(tb);
        if (query != "None") {
            tb.setRowFilter(RowFilter.regexFilter(query));
        } else {
            roomTable.setRowSorter(tb);
        }
    }

