    private void showTable(int page) {
        RoomList = RoomManagement.loadDataRoom(page);
        defaultTableModel.setRowCount(0);
        RoomList.forEach((rooms) -> {
            defaultTableModel.addRow(new Object[]{
                defaultTableModel.getRowCount() + 1,
                rooms.getRoom_number(), 
                rooms.getRoom_type(),
                rooms.getFloor(), 
                rooms.getStatus(),
                rooms.getPrice(),
                rooms.getImage()
            });
           
        });
    }
