public class Comparision {
    public static void main(String[] args) {
        int a= 100;
        int b= 200;
        System.out.println(" a<b? " + (a < b));
        System.out.println(" a>b? " + (a > b));
        System.out.println(" a<=b? " + (a <= b));
        System.out.println(" a>=b? " + (a >= b));
        System.out.println(" a==b? " + (a == b));
        System.out.println(" a!=b? " + (a != b));
    }
}
